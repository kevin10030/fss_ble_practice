    /**
     * remove掉preference里面字段
     * @param key 要remove掉的key
     */
    public static boolean removeKey(Context context, String key){
        SharedPreferences settings = context.getSharedPreferences(PREFERENCE_NAME, Context.MODE_PRIVATE);
        SharedPreferences.Editor editor = settings.edit();
        editor.remove(key);
        return editor.commit();   	
    }


    /**
     * Method to save the newly found Bluetooth Printer values
     * @param name The name of the bluetooth printer
     * @param uuid The defined UUID of the bluetooth printer
     * @param mac  The MAC address of the bluetooth printer
     */
    public static void saveDeviceSetting(Context context, String name, String uuid, String mac)
    {
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        SharedPreferences.Editor editor = prefs.edit();
        editor.putString(Constants.DEVICE_NAME, name);
        editor.putString(Constants.DEVICE_UUID, uuid);
        editor.putString(Constants.DEVICE_MAC,  mac);
        editor.commit();
    }


    public static HashMap<String, String> getDeviceSettings(Context context)
    {
        HashMap<String, String> printerHash = new HashMap<String, String>(3);
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        printerHash.put(Constants.DEVICE_NAME, prefs.getString(Constants.DEVICE_NAME, null));
        printerHash.put(Constants.DEVICE_UUID, prefs.getString(Constants.DEVICE_UUID, null));
        printerHash.put(Constants.DEVICE_MAC, prefs.getString(Constants.DEVICE_MAC, null));

        return printerHash;
    }


    public static void setBlePin( String address, String pin) {
        putString(AppController.getContext(), Constants.PREF_BLE_DEVICE_PIN + "_" + address, pin);
    }

